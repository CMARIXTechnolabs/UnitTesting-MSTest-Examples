namespace UnitTesting_MSTest_Examples
{
    /// <summary>
    /// Calculator class
    /// </summary>
    public class Calculator
    {
        /// <summary>
        /// Adds the specified x.
        /// </summary>
        /// <param name="x">The x.</param>
        /// <param name="y">The y.</param>
        /// <returns>Returns value after adding</returns>
        public int Add(int x, int y)
        {
            return x + y;
        }

        /// <summary>
        /// Subtracts the specified x.
        /// </summary>
        /// <param name="x">The x.</param>
        /// <param name="y">The y.</param>
        /// <returns>Returns value after subtracting</returns>
        public int Subtract(int x, int y)
        {
            return x - y;
        }
    }
}
